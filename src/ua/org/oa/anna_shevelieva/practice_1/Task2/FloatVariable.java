package ua.org.oa.anna_shevelieva.practice_1.Task2;

import java.util.*;

public class FloatVariable {
 
        public static void main(String[] args) {
 
                float a = (float) 1;
                System.out.println("Value of float variable a is :" + a); 
                
                float b = 1;
                System.out.println("Value of float variable b is :" + b); 
                
                float c = 0x1;
                System.out.println("Value of float variable c is :" + c); 
                
                float d = 0b1;
                System.out.println("Value of float variable d is :" + d); 
                
                float e = (float) 1.0e1;
                System.out.println("Value of float variable e is :" + e); 
                
                float f = 01;
                System.out.println("Value of float variable f is :" + f); 
                
                
        }
}